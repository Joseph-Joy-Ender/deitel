package task;

public class RowsAndColumn {
    public static void main(String[] args) {

        for (int row = 5; row >= 1; row--) {
            System.out.println("#");
            for (int column = 5; column >= 1 ; column++) {
                System.out.println();

            }
            System.out.print("*");
        }
    }
}
