package chapter1;

public class Area {

        public static void main(String[] args) {
            double radius = 2.5;

            double perimeter = 2 * radius * 3.14159;

            double area = radius * radius * 3.14159;

            System.out.printf("The perimeter is %.2f%n", + perimeter);

            System.out.printf("The area is %.4f%n", +  area );
        }
    }



