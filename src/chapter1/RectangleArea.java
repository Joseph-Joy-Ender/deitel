package chapter1;

public class RectangleArea {

    public static void main(String[] args) {

double width = 4.5;
double length = 17.0;
double height = 7.9;

double perimeter = (length + width) * 2;

double area = width * height;

System.out.printf("The perimeter of a rectangle is %.3f", perimeter);
System.out.println();

System.out.printf("The area of a rectangle is %.3f", area);

    }

}
