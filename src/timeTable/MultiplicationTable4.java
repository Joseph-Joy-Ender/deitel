package timeTable;

public class MultiplicationTable4 {
    public static void main(String[] args) {

        System.out.println("S/N " + "  ");

        int num = 4;

        for (int count = 1; count <= 12 ; count++) {
            System.out.printf("%d * %d = %d\n ", num, count, num * count);

        }
    }
}
